package ProgramLogic.entities;

public class step12z13 {
    public static void main(String[] args) {

        System.out.println("ASCEND ORDER");
        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }

        System.out.println("DESCEND ORDER");
        for (int i = 10; i >= 1; i--) {
            System.out.println(i);
        }
    }
    
}
